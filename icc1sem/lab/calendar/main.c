#include <stdio.h>

int main() {
    int today;
    scanf("%i", &today);

    printf("         Abril 2021         \n");
    printf(" Do  Se  Te  Qu  Qu  Se  Sa \n");
    printf("                ");

    int lineSlots = 3;
    for (int i = 1; i < 31; i++) {
        
        if (i == today) printf("(%2d)", i);
        else printf(" %2d ", i);

        if (--lineSlots == 0) {
            printf("\n");
            lineSlots = 7;
        }

    } printf("\n");
}